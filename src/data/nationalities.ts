// Fonction utilitaire pour obtenir les nationalités triées par ordre alphabétique
export const getSortedNationalities = () => {
  return [...NATIONALITIES].sort((a, b) => a.localeCompare(b, 'fr'));
};

// Fonction utilitaire pour rechercher une nationalité
export const searchNationality = (query: string) => {
  const normalizedQuery = query.toLowerCase().trim();
  return NATIONALITIES.filter(nationality => 
    nationality.toLowerCase().includes(normalizedQuery)
  );
};
